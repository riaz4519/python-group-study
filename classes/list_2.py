myList = [1,2,3,4,6]
print(myList)
myList.reverse()
print(myList)
print(myList[::-1])

#remove by value
myList.remove(3)
print(myList)

#reverse
myList.reverse()

#add value to the position insert(position,value)
myList.insert(2,3)
print(myList)

#pop() to remove
myList.pop()
print(myList)

#pop(index) to remove
myList.pop(2)
print(myList)
